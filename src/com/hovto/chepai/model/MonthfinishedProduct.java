package com.hovto.chepai.model;

public class MonthfinishedProduct {
	private String place;
	private int da;
	private int xiao;
	private int mo;
	private int nong;
	private int gua;
	private int jiao;
	public String getPlace() {
		return place;
	}
	public void setPlace(String place) {
		this.place = place;
	}
	public int getDa() {
		return da;
	}
	public void setDa(int da) {
		this.da = da;
	}
	public int getXiao() {
		return xiao;
	}
	public void setXiao(int xiao) {
		this.xiao = xiao;
	}
	public int getMo() {
		return mo;
	}
	public void setMo(int mo) {
		this.mo = mo;
	}
	public int getNong() {
		return nong;
	}
	public void setNong(int nong) {
		this.nong = nong;
	}
	public int getGua() {
		return gua;
	}
	public void setGua(int gua) {
		this.gua = gua;
	}
	public int getJiao() {
		return jiao;
	}
	public void setJiao(int jiao) {
		this.jiao = jiao;
	}
	
}
